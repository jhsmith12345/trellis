export default function SetupWizardPage() {
  const [currentStep, setCurrentStep] = useState(0);
  const [errors, setErrors] = useState<FieldError[]>([]);
  const contentRef = useRef<HTMLDivElement>(null);

  const [config, setConfig] = useState<WizardConfig>({
    projectId: "",
    region: "us-central1",
    saEmail: "",
    domain: "",
    senderEmail: "",
    dbPassword: "",
    cronSecret: "",
    firebaseApiKey: "",
    firebaseAuthDomain: "",
    firebaseProjectId: "",
    firebaseStorageBucket: "",
    firebaseMessagingSenderId: "",
    firebaseAppId: "",
    _baaConfirmed: false,
    _apisConfirmed: false,
    _delegationConfirmed: false,
  });

  const nextStep = useCallback(() => {
    const validationErrors = validateStep(currentStep, config);
    setErrors(validationErrors);
    if (validationErrors.length > 0) return;

    // Side effects on successful validation
    if (currentStep === 1) {
      // Auto-fill firebaseProjectId from projectId
      setConfig((c) => ({ ...c, firebaseProjectId: c.projectId }));
    }
    if (currentStep === 7) {
      // Auto-generate passwords if not provided
      setConfig((c) => ({
        ...c,
        dbPassword: c.dbPassword || generatePassword(24),
        cronSecret: c.cronSecret || generatePassword(32),
      }));
    }

    if (currentStep < TOTAL_STEPS - 1) {
      setCurrentStep((s) => s + 1);
      setErrors([]);
      window.scrollTo(0, 0);
    }
  }, [currentStep, config]);

  const prevStep = useCallback(() => {
    if (currentStep > 0) {
      setCurrentStep((s) => s - 1);
      setErrors([]);
      window.scrollTo(0, 0);
    }
  }, [currentStep]);

  const progressPct = (currentStep / (TOTAL_STEPS - 1)) * 100;

  return (
    <div className="min-h-screen bg-warm-50 flex items-start justify-center px-4 py-8">
      {/* Custom CSS for instruction list counters */}
      <style>{`
        .counter-reset-step { counter-reset: step; }
        .instruction-item { counter-increment: step; }
        .instruction-item::before {
          content: counter(step);
          position: absolute;
          left: 0;
          top: 0.75rem;
          width: 1.5rem;
          height: 1.5rem;
          background: #e8ece8;
          color: #3d5a4a;
          border-radius: 50%;
          font-size: 0.75rem;
          font-weight: 600;
          display: flex;
          align-items: center;
          justify-content: center;
        }
      `}</style>

      <div className="w-full max-w-[720px] bg-white rounded-2xl shadow-sm border border-warm-100 overflow-hidden">
        {/* Header */}
        <header className="flex items-center justify-between px-6 py-4 border-b border-warm-100">
          <Link to="/" className="flex items-center gap-3 hover:opacity-80 transition-opacity">
            <svg viewBox="0 0 32 32" width="32" height="32" fill="none">
              <rect width="32" height="32" rx="8" fill="#4f6d5e" />
              <path
                d="M8 12h16M12 8v16M20 8v16"
                stroke="#fff"
                strokeWidth="2"
                strokeLinecap="round"
              />
            </svg>
            <span className="font-display text-xl font-bold text-warm-800">
              Trellis
            </span>
          </Link>
          <span className="text-sm text-warm-500 font-medium">
            Setup Wizard
          </span>
        </header>

        {/* Progress bar */}
        <div className="h-1 bg-warm-100">
          <div
            className="h-full bg-teal-700 transition-all duration-300 ease-out rounded-r-sm"
            style={{ width: `${progressPct}%` }}
          />
        </div>

        {/* Step dots */}
        <div className="flex justify-center gap-2 pt-4 px-6">
          {Array.from({ length: TOTAL_STEPS }).map((_, i) => (
            <div
              key={i}
              className={`w-2 h-2 rounded-full transition-all duration-300 ${
                i === currentStep
                  ? "bg-teal-700 scale-125"
                  : i < currentStep
                    ? "bg-teal-500"
                    : "bg-warm-200"
              }`}
              title={STEP_NAMES[i]}
            />
          ))}
        </div>

        {/* Step content */}
        <div ref={contentRef} className="px-6 py-8 min-h-[400px]">
          {currentStep === 0 && <StepWelcome />}
          {currentStep === 1 && (
            <StepGcpProject
              config={config}
              setConfig={setConfig}
              errors={errors}
            />
          )}
          {currentStep === 2 && (
            <StepBaa config={config} setConfig={setConfig} errors={errors} />
          )}
          {currentStep === 3 && (
            <StepApis config={config} setConfig={setConfig} errors={errors} />
          )}
          {currentStep === 4 && (
            <StepServiceAccount
              config={config}
              setConfig={setConfig}
              errors={errors}
            />
          )}
          {currentStep === 5 && (
            <StepDelegation
              config={config}
              setConfig={setConfig}
              errors={errors}
            />
          )}
          {currentStep === 6 && (
            <StepFirebase
              config={config}
              setConfig={setConfig}
              errors={errors}
            />
          )}
          {currentStep === 7 && (
            <StepDomain
              config={config}
              setConfig={setConfig}
              errors={errors}
            />
          )}
          {currentStep === 8 && <StepDeploy config={config} />}
        </div>

        {/* Footer navigation */}
        <footer className="flex items-center justify-between px-6 py-4 border-t border-warm-100 bg-warm-50">
          {currentStep > 0 ? (
            <button
              onClick={prevStep}
              className="px-5 py-2.5 text-sm font-medium text-warm-600 bg-white border border-warm-200 rounded-lg hover:bg-warm-50 hover:border-warm-300 transition-all"
            >
              Back
            </button>
          ) : (
            <div />
          )}

          <span className="text-[0.8125rem] text-warm-500">
            Step {currentStep + 1} of {TOTAL_STEPS}
          </span>

          {currentStep < TOTAL_STEPS - 1 ? (
            <button
              onClick={nextStep}
              className="px-5 py-2.5 text-sm font-medium text-white bg-teal-700 rounded-lg hover:bg-teal-800 transition-all"
            >
              Continue
            </button>
          ) : (
            <div />
          )}
        </footer>
      </div>
    </div>
  );
}
