function CodeBlock({ text }: { text: string }) {
  const [copied, setCopied] = useState(false);

  const handleCopy = useCallback(() => {
    navigator.clipboard.writeText(text).then(() => {
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    });
  }, [text]);

  return (
    <div className="relative bg-warm-800 text-warm-200 rounded-lg font-mono text-[0.8125rem] leading-relaxed overflow-x-auto my-3">
      <button
        onClick={handleCopy}
        className="absolute top-2 right-2 px-2 py-1 text-[0.7rem] bg-white/10 border border-white/20 text-warm-200 rounded hover:bg-white/20 hover:text-white transition-all"
      >
        {copied ? "Copied!" : "Copy"}
      </button>
      <pre className="p-4 pr-20 whitespace-pre-wrap break-all">{text}</pre>
    </div>
  );
}

