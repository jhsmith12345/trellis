// ── Validation Functions ──

function validateStep(step: number, config: WizardConfig): FieldError[] {
  const errors: FieldError[] = [];

  switch (step) {
    case 0: // Welcome
      break;

    case 1: {
      // GCP Project
      const id = config.projectId;
      if (!id) {
        errors.push({ field: "projectId", message: "Project ID is required" });
      } else if (!/^[a-z][a-z0-9-]{4,28}[a-z0-9]$/.test(id)) {
        errors.push({
          field: "projectId",
          message:
            "Must be 6-30 characters: lowercase letters, numbers, hyphens. Must start with a letter.",
        });
      }
      break;
    }

    case 2: // BAA
      if (!config._baaConfirmed) {
        errors.push({
          field: "_baaConfirmed",
          message: "You must confirm BAA signing",
        });
      }
      break;

    case 3: // APIs
      if (!config._apisConfirmed) {
        errors.push({
          field: "_apisConfirmed",
          message: "You must confirm APIs are enabled",
        });
      }
      break;

    case 4: {
      // Service Account
      const email = config.saEmail;
      if (!email) {
        errors.push({
          field: "saEmail",
          message: "Service account email is required",
        });
      } else if (!email.endsWith(".iam.gserviceaccount.com")) {
        errors.push({
          field: "saEmail",
          message: "Must end with .iam.gserviceaccount.com",
        });
      } else if (!email.includes("@")) {
        errors.push({ field: "saEmail", message: "Invalid email format" });
      }
      break;
    }

    case 5: {
      // Delegation
      if (!config.senderEmail) {
        errors.push({
          field: "senderEmail",
          message: "Sender email is required",
        });
      } else if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(config.senderEmail)) {
        errors.push({
          field: "senderEmail",
          message: "Invalid email format",
        });
      }
      if (!config._delegationConfirmed) {
        errors.push({
          field: "_delegationConfirmed",
          message: "You must confirm delegation setup",
        });
      }
      break;
    }

    case 6: {
      // Firebase
      if (!config.firebaseApiKey) {
        errors.push({
          field: "firebaseApiKey",
          message: "API key is required",
        });
      } else if (!config.firebaseApiKey.startsWith("AIza")) {
        errors.push({
          field: "firebaseApiKey",
          message: 'Firebase API keys typically start with "AIza"',
        });
      }
      if (!config.firebaseAuthDomain) {
        errors.push({
          field: "firebaseAuthDomain",
          message: "Auth domain is required",
        });
      }
      if (!config.firebaseStorageBucket) {
        errors.push({
          field: "firebaseStorageBucket",
          message: "Storage bucket is required",
        });
      }
      if (!config.firebaseMessagingSenderId) {
        errors.push({
          field: "firebaseMessagingSenderId",
          message: "Messaging Sender ID is required",
        });
      } else if (!/^\d+$/.test(config.firebaseMessagingSenderId)) {
        errors.push({
          field: "firebaseMessagingSenderId",
          message: "Sender ID should be numeric",
        });
      }
      if (!config.firebaseAppId) {
        errors.push({ field: "firebaseAppId", message: "App ID is required" });
      }
      break;
    }

    case 7: {
      // Domain
      if (!config.domain) {
        errors.push({ field: "domain", message: "Domain is required" });
      } else if (
        !/^[a-z0-9]([a-z0-9-]*[a-z0-9])?(\.[a-z0-9]([a-z0-9-]*[a-z0-9])?)+$/.test(
          config.domain.toLowerCase()
        )
      ) {
        errors.push({
          field: "domain",
          message: "Enter a valid domain (e.g., app.yourpractice.com)",
        });
      }
      break;
    }

    case 8: // Deploy - no validation
      break;
  }

  return errors;
}

